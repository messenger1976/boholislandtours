                    <h3>Public Transport</h3>
                    <p>Travelling around Bohol is quite an easy experience even for non seasoned visitors. Below are the common means of transportation in the city/province:</p>
                    <ul>
                        <li><strong>Tricycle</strong> – there are tricycles (motorcycle with sidecars), habal-habal motorbikes with minimal fare which are suitable for compact city streets and journeys of short distances.</li>
                        <li><strong>Jeepney</strong> – a Jeepney will cost 8 pesos flat fare, however some routes are very long and can cost you a little more. Typically, jeepneys will not leave until completely full, so be prepared to wait a while.</li>
