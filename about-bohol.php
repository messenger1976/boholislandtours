                    <h2>Climate</h2>
                    <p>The climate in Bohol is very pleasant and warm. From November to April, the northeast monsoon (<em>amihan</em>) prevails. Except for a rare shower, this is the mildest time of the year. Daytime temperatures average 28°C, cooling down at night to around 25°C. The summer season from May to July brings higher temperatures and very humid days. From August to October is the southwest monsoon (<em>habagat</em>). The weather during this season is not very predictable, with weeks of calm weather alternating with rainy days. It can rain any day of the year, but a higher chance of heavy showers occurs from November to January.</p>
