                <div class="info-section" id="dress">
                    <h2>Dress/What to bring</h2>
                    <p>Bohol is indeed an interesting place to visit as it is rich in historical sites and beautiful beaches. The climate is very pleasant and warm. Visitors can spend days touring the city as they wouldn't run out of things to do. However, what to bring and what to wear in Bohol is a major concern for most travelers. In an effort to help, we have narrowed down a list of things to bring when visiting Bohol:</p>
                    <ul>
                        <li>Make sure to wear light clothing made of natural fiber.</li>
                        <li>Bring a hat or cap with you to protect your scalp from the heat of the sun, especially in open-spaced venues. Bring sun glasses to protect your eyes.</li>
                        <li>Apply sun block before leaving your hotel or accommodations. Re-apply significantly when bathing in beaches and swimming pools.</li>
                        <li>Bring comfortable shoes for touring around the city and rubber slippers when lounging around the beaches.</li>
                        <li>When headed to any Bohol's pristine beaches, make sure to bring the following: shorts, bathing suit, cover-ups, flashlights and loads of sun block.</li>
                        <li>Aside from applying sun block to your skin, make sure to apply insect repellant as well to protect yourself from mosquitoes. Do this especially in visiting lush vegetation areas or forest as you may be visiting a haven for insects.</li>
                        <li>A small, handy bag should be perfect to house your belongings safely.</li>
                        <li>Shorts, sleeveless t-shirts or slippers should never be worn when visiting churches, mosques or temples. These areas are considered sacred places, so do your part and respect them.</li>
                        <li>The Barong Tagalog is acceptable formal wear when attending social events around the city. Bring something a bit formal for the ladies just in case you attend any social events.</li>
                        <li>Of course, never forget to bring your camera.</li>
                    </ul>
                </div>

