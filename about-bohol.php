                    <h2>Money</h2>
                    <p>The Philippine currency is peso. Next to the peso, the US dollar which is widely accepted. Most foreign currencies may be change at banks, hotels, and authorized dealers. Foreign currencies are not accepted by the establishments in Bohol. Major foreign credit cards may be accepted only in major hotels, resorts, shops and restaurants. Visitors/guest are advised to bring enough amount of change in small coins or bills.</p>
